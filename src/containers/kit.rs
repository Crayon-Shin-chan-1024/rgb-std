use amplify::confinement::TinyOrdMap;
use rgb::SubSchema;

use crate::interface::{Iface, IfaceImpl};
use crate::LIB_NAME_RGB_STD;

/// A set of data needed by contract issuers to issue a contract under some
/// schema, and implementing a (set of) interface(s).
#[derive(Clone, Eq, PartialEq, Ord, PartialOrd, Hash, Debug)]
#[derive(StrictType, StrictDumb, StrictEncode, StrictDecode)]
#[strict_type(lib = LIB_NAME_RGB_STD)]
#[cfg_attr(
    feature = "serde",
    derive(Serialize, Deserialize),
    serde(crate = "serde_crate", rename_all = "camelCase")
)]
pub struct Kit {
    pub schema: SubSchema,
    pub ifaces: TinyOrdMap<Iface, IfaceImpl>,
}
